package book.vo;

public class Book {
	private String bookNum;
	
	private String libCode;
	private String bookName;
	private String bookQty;
	private String bookPub;
	private String bookPubDate;
	private String bookWriter;
	private String ISBN;
	private String bookRegDate;
	private String bookState;
	private String bookImage;
	private String bookCategory;
	
	public String getBookNum() {
		return bookNum;
	}
	public void setBookNum(String bookNum) {
		this.bookNum = bookNum;
	}
	public String getLibCode() {
		return libCode;
	}
	public void setLibCode(String libCode) {
		this.libCode = libCode;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getBookQty() {
		return bookQty;
	}
	public void setBookQty(String bookQty) {
		this.bookQty = bookQty;
	}
	public String getBookPub() {
		return bookPub;
	}
	public void setBookPub(String bookPub) {
		this.bookPub = bookPub;
	}
	public String getBookPubDate() {
		return bookPubDate;
	}
	public void setBookPubDate(String bookPubDate) {
		this.bookPubDate = bookPubDate;
	}
	public String getBookWriter() {
		return bookWriter;
	}
	public void setBookWriter(String bookWriter) {
		this.bookWriter = bookWriter;
	}
	public String getISBN() {
		return ISBN;
	}
	public void setISBN(String string) {
		ISBN = string;
	}
	public String getBookRegDate() {
		return bookRegDate;
	}
	public void setBookRegDate(String bookRegDate) {
		this.bookRegDate = bookRegDate;
	}
	public String getBookState() {
		return bookState;
	}
	public void setBookState(String bookState) {
		this.bookState = bookState;
	}
	public String getBookImage() {
		return bookImage;
	}
	public void setBookImage(String bookImage) {
		this.bookImage = bookImage;
	}
	public String getBookCategory() {
		return bookCategory;
	}
	public void setBookCategory(String bookCategory) {
		this.bookCategory = bookCategory;
	}
	
}
